# Belajar operator assignment

print('======Operator Assignment Python======')
a = 8
print('nilai a adalah = ', a)

# Menggunakan operasi artimatika
a += 3
print('hasilnya adalah =', a)

a -= 2
print('hasilnya adalah =', a)

a *= 2
print('hasilnya adalah =', a)

a /= 4
print('hasilnya adalah =', a)

# operator modulus
b = 20
print('nilai b adalah =', b)

b %= 18
print('hasilnya adalah =', b)

# Operator eksponen/pangkat
c = 9
print('nilai c adalah =', c)

c **= 3
print('hasilnya adalah =', c)

# Operator bitwise 
d  = True
print('nilai d adalah =', d)

d |= False
print('hasil dari True or False adalah =', d)

e = False
print('nilai e adalah =', e)

e |= False
print('hasil dari False or False adalah =', e)
