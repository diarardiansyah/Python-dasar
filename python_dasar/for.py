# for dengan angka list
angka_list = [0,1,2,3,4,5,6]

# Perulangan for dengan angka list
for i in angka_list:
    print(f"hasil dari perulangan for angka list adalah {i}")

print("akhir dari program 1 \n")

# for dengan range angka 
range_angka = range(6)

# Perulangan for dengan range 
for i in range_angka:
    if i >= 1:
        print("saya tampan")

print("akhir dari program 2 \n")

# for dengan range 
range1 = range(1,12)

for i in range1:
    print(f"hasil dari perulangan nya adalah {i}")

print("akhir dari program 3 \n")

